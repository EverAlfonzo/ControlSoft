    public void generarReporte(jDSeleccionPrecio jDTipoPrecio,String tipoPrecio,String tipoReporte,Date a,Date b){
        
        String sOrderBy;
        if(tipoReporte.equals("volumen"))
            sOrderBy="cuenta";
        else
            sOrderBy="SubTotal";
        
        ResultSet rs = null;
            rs=db.Listar("DISTINCT `producto`.`Cod_inter_producto`,`Marca_Fabrica`,`Cod_Original`,`Nom_producto`,`Marca_producto`,`detalle_producto`,`ubica_producto`,`precio_n_venta`,`Cant_producto`,`cant_min`,`pre_compra`,`Proveedor_Id_proveedor`,`ultima_venta`,`imagen`,`ruta_imagen`"
                    + " ,COUNT(*)  AS cuenta ,SUM(precio_n_venta*Cant_vendida) AS SubTotal,SUM(`detalle_venta`.`Cant_vendida`) AS CantVendida",
                    "detalle_venta,producto,venta",
                    "where `producto`.`Cod_inter_producto`=`detalle_venta`.`Cod_inter_producto` and `venta`.`Id_venta`=`detalle_venta`.`Venta_Id_venta` and `Fecha_venta` >= '"+(a.getYear()+1900)+"-"
                      +(a.getMonth()+1)+"-"+(a.getDate())+"' and `Fecha_venta` <= '"+(b.getYear()+1900)+"-" 
                      +(b.getMonth()+1)+"-"+(b.getDate())+"' and `detalle_venta`.`nro_precio`= '"+tipoPrecio+"' group by `producto`.`Cod_inter_producto` order by "+sOrderBy+" DESC limit 30");


            JRResultSetDataSource jrRS = new JRResultSetDataSource(rs);
            HashMap parameters = new HashMap();
            parameters.clear();

            parameters.put("TipoReporte", tipoReporte);
            parameters.put("TipoPrecio",jDTipoPrecio.getPrecio());
            try{
                this.setCursor(Cursor.WAIT_CURSOR);
                URL urlMaestro = getClass().getClassLoader().getResource("reportes/ProductoRotacionReport.jasper");
                JasperReport masterReport = null;
                masterReport = (JasperReport) JRLoader.loadObject(urlMaestro);
                JasperPrint masterPrint = null;
                masterPrint = JasperFillManager.fillReport(masterReport,parameters,jrRS);
                JasperViewer ventana = new JasperViewer(masterPrint,false);
                ventana.setTitle("Vista Previa");
                ventana.setModalExclusionType(Dialog.ModalExclusionType.APPLICATION_EXCLUDE);
                ventana.setVisible(true);
                this.setCursor(null);
            }catch(JRException e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Ocurrio un error "+e.toString(),"ATENCION ", JOptionPane.INFORMATION_MESSAGE);
            }
            jDTipoPrecio.dispose();
    }
    
