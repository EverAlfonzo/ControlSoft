    private void jMVentsDiasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMVentsDiasActionPerformed
        jDViewDate a = new jDViewDate(this, true,"Seleccione una Fecha:");
        a.setVisible(true);
        ResultSet r=null;
        if(a.fecha!=null){
            
         try{
           r=db.Listar("*", "vistaventa", "where `Fecha_venta` >= '"+(a.fecha.getYear()+1900)+"-"
                   +(a.fecha.getMonth()+1)+"-"+(a.fecha.getDate())+"' order by Id_venta ASC");
          

           
        } catch (Exception ex) {
             //Logger.getLogger(ciudad.class.getName()).log(Level.SEVERE, null, ex);
        }

        JRResultSetDataSource jrRS = new JRResultSetDataSource(r);
        HashMap parameters = new HashMap();
        
        try{
            URL urlMaestro = getClass().getClassLoader().getResource("reportes/Ventas.jasper");
            // Cargamos el reporte
            JasperReport masterReport = null;
            masterReport = (JasperReport) JRLoader.loadObject(urlMaestro);
            JasperPrint masterPrint = null;
            masterPrint = JasperFillManager.fillReport(masterReport,parameters,jrRS);
               
            
            
             JasperViewer ventana = new JasperViewer(masterPrint,false);
            ventana.setTitle("Vista Previa");
            ventana.setModalExclusionType(Dialog.ModalExclusionType.APPLICATION_EXCLUDE);
            ventana.setVisible(true);

        }catch(JRException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Ocurrio un error "+e.toString(),"ATENCION ", JOptionPane.INFORMATION_MESSAGE);
        }
    }                                        

     
        
    }//GEN-LAST:event_jMVentsDiasActionPerformed

