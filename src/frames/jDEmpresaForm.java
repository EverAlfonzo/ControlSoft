    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        boolean exito=false;
        if(id.equals("")){
            String empresa = jTNombre.getText();
            String ruta = JTRuta.getText();
            String datos = jTInfoContacto.getText();
            
            if(!(empresa.equals("")&&ruta.equals("")&&datos.equals(""))){
                FileInputStream foto = null;
                try{
                    foto = new FileInputStream(ruta);
                }catch(FileNotFoundException ex){
                    JOptionPane.showMessageDialog(rootPane, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                }
                
                if(con.AltasEmpresa(empresa, datos, foto)){
                    exito = true;
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Por favor llene los campos", "Atencion", JOptionPane.WARNING_MESSAGE);
            }  
        }else{
            String empresa = jTNombre.getText();
            String ruta = JTRuta.getText();
            String datos = jTInfoContacto.getText();

            if(!(empresa.equals(nombre_ini)&&ruta.equals("Sin cambios")&&datos.equals(datos_ini))){
                if(!ruta.equals("Sin cambios")){
                    FileInputStream foto = null;
                    try{
                        foto = new FileInputStream(ruta);
                    }catch(FileNotFoundException ex){
                        JOptionPane.showMessageDialog(rootPane, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                    }
                    if (con.actualizarEmpresa(empresa,datos, foto, id)){
                        exito = true;
                    }
                }
                if (con.actualizarRegistro("empresa","nombre = '"+empresa, "', "
                        + "datos = '"+datos+"' where id_empresa = "+id)){
                    exito = true;
                }
            }
        }
        if(exito){
            JOptionPane.showMessageDialog(rootPane, "Los cambios fueron guardados", "Exito", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(rootPane, "OcurriÃ³ un error al guardar los cambios", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBAceptarActionPerformed

