    public void llenar(String id){
        ResultSet r=null;
        r = con.Listar("nombre, datos", "empresa", "where id_empresa= '"+id+"'");
        try {
            r.next();
            jTNombre.setText(r.getString("nombre"));
            nombre_ini = jTNombre.getText();
            JTRuta.setText("Sin cambios");
            jTInfoContacto.setText(r.getString("datos"));
            datos_ini = jTInfoContacto.getText();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,
                    ex.getMessage(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
        mostrarImagen(id);
     }
    
    public void mostrarImagen(String id){
        ResultSet rs = con.Listar("*", "empresa", "where id_empresa= '"+id+"'");

        try{
            rs.next();
            Blob blob = (Blob) rs.getBlob("imagen");
            byte [] data = blob.getBytes(1,(int) blob.length());
            BufferedImage img = null;
            img = ImageIO.read(new ByteArrayInputStream(data));
            jXImageView1.setImage(img);

        } catch ( Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

