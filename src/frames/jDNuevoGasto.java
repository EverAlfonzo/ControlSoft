    private void jBRegGastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegGastoActionPerformed
        ResultSet rs=null;
        boolean bandera=false; // informa acerca de la existencia o no de un item igual en la tabla
        String cod1;
        String cod2;
        int indice=0;
        if(!jXTCategoria.getText().equals("")){
           jBRegGastos.setEnabled(true);
           rs= db.Listar("*", "categoria", "where Nom_categoria='"+jXTCategoria.getText()+"'");
           String vec[] = new String[3];
           
           int hasta = jTable1.getRowCount();
           
           for(int i=0;i<hasta;i++){
               bandera=false;
               cod1 = String.valueOf(jTable1.getValueAt(i, 1));
               cod2 = jXTCategoria.getText();
               if(cod1.equals(cod2)){
                   bandera=true;
                   indice = i;
                   break;
               }
           }
           //si la categoria no se ha registrado aún en el gasto actual
           if(!bandera){
               try {
                   //se verifica si esta categoría ya esta registrada en la base de datos
                   if(rs.next()){
                       //si ya se había guardado antes, entonces sólo se agrega
                       //la categoria al gasto actual
                       vec[0]= rs.getString("Id_categoria");
                       vec[1]= rs.getString("Nom_categoria");
                       vec[2]= jTPrecio.getText();
                       
                       m.addRow(vec);
                       total=total + Integer.valueOf(jTPrecio.getText());
                       jTGasTotal.setText(String.valueOf(total));
                       actualizarCampoCategoria();
                   }
               } catch (SQLException ex) {
                   JOptionPane.showMessageDialog(rootPane,ex.getMessage(), "Error Al acceder a la base de datos", JOptionPane.ERROR_MESSAGE);
               }
           }else{
               //si la categoría ya se había registrado en el gasto actual
               total=total+Integer.valueOf(jTPrecio.getText());
               jTGasTotal.setText(String.valueOf(total));
               jTable1.setValueAt(String.valueOf(Integer.valueOf(jTPrecio.getText())+Integer.valueOf((String) jTable1.getValueAt(indice,2))),indice, 2);
           }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Debe introducir el nombre de categoria de gasto", "Atención", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBRegGastoActionPerformed

