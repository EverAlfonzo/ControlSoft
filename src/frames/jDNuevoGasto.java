    public final void actualizarCampoCategoria(){
     
        List<String> lista = new ArrayList<String>();
        try {
           ResultSet rs=null;
            rs= db.Listar("Nom_categoria", "categoria", "");
            while(rs.next()){
                lista.add(rs.getString(1));
            }
            AutoCompleteDecorator.decorate(jXTCategoria,lista,false);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(),"Error Al acceder a la base de datos",JOptionPane.ERROR_MESSAGE);
        }
        
    }
