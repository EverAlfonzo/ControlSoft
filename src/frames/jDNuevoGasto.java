    private void jXTCategoriaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jXTCategoriaKeyReleased
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            ResultSet rs = db.Listar("Desc_categoria", "categoria", "where  Nom_categoria='"+jXTCategoria.getText()+"'");
            try {
                if(rs.next()){
                    jTDescCat.setText(rs.getString(1));
                }else{
                    String desc = JOptionPane.showInputDialog(rootPane, "Descripción de Categoría", "Categoria", JOptionPane.QUESTION_MESSAGE);
                    db.Altas("categoria", "(Nom_categoria,Desc_categoria)", "('"+jXTCategoria.getText()+"','"+desc+"')");
                    jTDescCat.setText(desc);
                    jTDescCat.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(jDNuevoGasto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jXTCategoriaKeyReleased

    private void jBRegGastosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegGastosActionPerformed
        int filas = jTable1.getRowCount();
        String obs = JOptionPane.showInputDialog(rootPane, "Introduzca alguna observación del gasto registrado", "Observación", JOptionPane.QUESTION_MESSAGE);
        
        if(db.Altas("gastos", "(fecha_gasto,obs_gasto)", "('"+(a.getYear()+1900)+"/"+(a.getMonth()+1)+"/"+a.getDate()+"','"+obs+"')")){
            ResultSet rs = db.Listar("Id_gasto", "gastos", "order by Id_gasto ASC");
            String id_gasto="1";
            try {
                if(rs.next()){
                    rs.last();
                    id_gasto= rs.getString(1);
                }
            } catch (SQLException ex) {
                Logger.getLogger(jDNuevoGasto.class.getName()).log(Level.SEVERE, null, ex);
            }
            for(int i=0;i<filas;i++){
                db.Altas("detalle_gasto", "","("+id_gasto+",'"+jTable1.getValueAt(i, 0)+"','"+jTable1.getValueAt(i, 2)+"')");
            }
        }
    }//GEN-LAST:event_jBRegGastosActionPerformed

