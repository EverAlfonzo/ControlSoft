    private void PrepararTabla() {
        double utilidadTotal=0;
        double total=0;
        r= null;
        if(jCBCategoria.getSelectedIndex()>0){
            r = bd.Listar("*", "vistaventa_categoria", "where `Fecha_venta` >= '"+(fecha.getYear()+1900)+"-"+(fecha.getMonth()+1)+"-"+(fecha.getDate())+"' and Fecha_venta <= '"+(hoy.getYear()+1900)+"-"+(hoy.getMonth()+1)+"-"+(hoy.getDate())
                +"' and  (nom_producto like \"%"+jTBusqueda.getText()+"%\") and categoria_id ="+jCBCategoriaId.getSelectedItem().toString()+"  order by fecha_venta DESC");
        }else{
            r = bd.Listar("*", "vistaventa_categoria", "where `Fecha_venta` >= '"+(fecha.getYear()+1900)+"-"+(fecha.getMonth()+1)+"-"+(fecha.getDate())+"' and Fecha_venta <= '"+(hoy.getYear()+1900)+"-"+(hoy.getMonth()+1)+"-"+(hoy.getDate())
                +"' and  nom_producto like \"%"+jTBusqueda.getText()+"%\" order by fecha_venta DESC");
        }
        
        
        m= (new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {"Numero de Fabrica","Producto","Marca","Detalle","Cantidad Vendida","Precio Unitario","Fecha","Total","Utilidad"})
             {
                 boolean[] canEdit = new boolean [] {false, false,false, false, false, false,false,false, false};

                 public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit [columnIndex];
                }});
        jTVentas.setModel(m);
        String Fila[]= new String[9];
            try {
                while(r.next()){
                    
                    Fila[0]= r.getString("marca_fabrica");
                    Fila[1]= r.getString("nom_producto");
                    Fila[2]= r.getString("marca_producto");
                    Fila[3]= r.getString("detalle_producto");
                    Fila[4]= r.getString("cant_vendida");
                    Fila[5]= r.getString("precio_n_venta");
                    Fila[6] = r.getString("fecha_venta");
                    Double pre_compra = Double.valueOf( String.valueOf(r.getString("pre_compra")).replace(".", ""));
                    Double cantidad_vendida = Double.valueOf(Fila[4]);
                    Double precio_n_venta = Double.valueOf(Fila[5]);
                    Double total_venta = cantidad_vendida*precio_n_venta;
                    Double total_compra = cantidad_vendida*pre_compra;
                    Fila[7] = String.valueOf(total_venta);
                    Fila[8] = String.valueOf(total_venta-total_compra);
                    m.addRow(Fila);
                    utilidadTotal+=Double.valueOf(Fila[8]);
                    total+=Double.valueOf(Fila[7]);
                    System.out.println(total);
                }
            } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, ex.getMessage(),"Error1",JOptionPane.ERROR_MESSAGE);
            }
            
            jTUtilidad.setText(String.valueOf(formatter.format(Double.valueOf(utilidadTotal).longValue())));
            jTTotal.setText(String.valueOf(formatter.format(Double.valueOf(total).longValue())));
        }
    
    public void PrepararComboCategoria(){
        ResultSet rs = bd.Listar("*", "categoria_producto", "");
        try {
            while(rs.next()){
                jCBCategoria.addItem(rs.getString("nombre_categoria"));
                jCBCategoriaId.addItem(rs.getString("codigo_categoria"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(jDVentasCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
